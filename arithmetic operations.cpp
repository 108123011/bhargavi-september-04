// the program is for finding the addition of two numbers //
#include<stdio.h>
int main( )
{
	int a,b;
	 scanf("%d %d" ,&a,&b);
	 int c=a+b;
	 int d=a-b;
	 int e=a*b;
	 int f=a/b;
	 printf("the addition of two numbers is %d \n",c);
	 printf("the subtraction of two numbers is %d \n",d);
	 printf("the multiplication of two numbers is %d \n",e);
	 printf("the division of two numbers is %d \n",f);
	 return 0;
}
